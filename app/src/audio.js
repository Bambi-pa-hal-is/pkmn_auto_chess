export function getBackgroundAudio(name) {
  switch(name) {
    case 'mainMenu':
      return goldThemeOriginal;
    case 'battle':
    case 'gymbattle':
    case 'pvpbattle':
      return trainerBattle;
    case 'rival':
      return rivalTheme;
    case 'wonGame':
      return gymVictoryTheme;
    case 'shop':
    case 'idle':
    default:  
      return pokemonCenter;
  }
}

export function getSoundEffect(name){
  switch(name){
    case 'sellUnit':
      return sellSound;
    case 'cheer':
      return cheer;
    case 'lvlup':
      return lvlup;
    case 'pling':
      return pling;
    case 'invalid':
      return invalid;
    case 'horn':
      return horn;
    case 'battleLose':
      return battleLose;
    case 'disconnect':
      return heGone;
    case 'Tick':
      return Tick;
    default:
      return undefined;
  }
}